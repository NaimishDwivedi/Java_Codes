public class switch_to_if_else {
    public static void main(String args[])
    {
        int x = 5;
        int m = 2;

        if (m == 0){
            x = x+2;
            System.out.println("X: "+x);
        }

        if (m == 1){
            x = x+4;
            System.out.println("X: "+x);
        }

        if (m == 2){
            x = x+6;
            System.out.println("X : "+x);
        }
    }

    
}
